# Moves the cursor to the next line
print("Hello\nWorld")


# Adds a horizontal tab (spacing).
print("Name\tAge")
print("John\t25")

# Prints a backslash
print("C:\\Users\\Admin")

#Used when the string is enclosed in single quotes.
print('It\'s a sunny day')

#Used when the string is enclosed in double quotes.
print("He said, \"Hello!\"")

#Moves cursor one position back.
print("Hello\bWorld")  

# Moves cursor to the beginning of the line
print("Hello\rWorld")

# Form feed - moves to the next page (used in printing)
print("Hello\fWorld")

# Vertical tab - moves the cursor down to the next tab stop
print("Hello\vWorld")

print("\u2764")  # Heart symbol

